import math

def	add(num1, num2):
	return num1 + num2

def	subtract(num1, num2):

	return num1 - num2

def	multiply(num1, num2):
	return num1 * num2

def	divide(num1, num2):
	return num1 / num2

def	power(num1, num2):
	return num1 ** num2

def	square_root(num1):
	return num1 ** 0.5

def	sine(num1):
	return math.sin(num1)

def	cosine(num1):
	return math.cos(num1)

def	tangent(num1):
	return math.tan(num1)

